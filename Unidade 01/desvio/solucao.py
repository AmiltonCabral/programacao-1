# Calcular distância ente N até uma sequência númerica

num_a = float(input())
num_b = float(input())
num_c = float(input())
num_n = float(input())

num_media = (num_a + num_b + num_c) / 3
num_resultado = num_n - num_media

print(f'{num_resultado:.1f}')
